namespace Sylius\Component\Affiliate\Model;

use Sylius\Component\Resource\Model\Image;

class Banner extends Image implements BannerInterface
{
    /**
     * Name.
     *
     * @var string
     */
    protected $name;

    /**
     * Status.
     *
     * @var bool
     */
    protected $status = true;

    /**
     * {@inheritdoc}
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * {@inheritdoc}
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * {@inheritdoc}
     */
    public function isEnabled()
    {
        return $this->status;
    }

    /**
     * {@inheritdoc}
     */
    public function getStatus()
    {
        return $this->status;
    }

    /**
     * {@inheritdoc}
     */
    public function setStatus($status)
    {
        $this->status = $status;

        return $this;
    }
}
